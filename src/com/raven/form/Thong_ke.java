    private void cbbLocItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbbLocItemStateChanged
        // TODO add your handling code here:
        String TenDanhMuc = cbbLoc.getSelectedItem().toString();
        if (TenDanhMuc.equals("Ngày")) {
            lblSanPham.setText(String.valueOf(repo.getbyday()));
            lblHoaDon.setText(String.valueOf(repo.gethdday()));
            lblDoanhThu.setText(CurrencyConvert.formatCurrency(repo.getdtday()));

            loadSP(repo.getsp());

        } else if (TenDanhMuc.equals("Tháng")) {
            lblSanPham.setText(String.valueOf(repo.getSPbyMonth()));
            lblHoaDon.setText(String.valueOf(repo.gethdMonth()));
            lblDoanhThu.setText(CurrencyConvert.formatCurrency(repo.getdtMonth()));

            loadSP(repo.getspByMonth());

        } else if (TenDanhMuc.equals("Năm")) {
            lblSanPham.setText(String.valueOf(repo.getSPbyYear()));
            lblHoaDon.setText(String.valueOf(repo.gethdYear()));
            lblDoanhThu.setText(CurrencyConvert.formatCurrency(repo.getdtYear()));

            loadSP(repo.getspbyYear());

        }
    }//GEN-LAST:event_cbbLocItemStateChanged

