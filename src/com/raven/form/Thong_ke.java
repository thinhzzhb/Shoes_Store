    private void loadSP(List<ThongKe> _lstTK) {
        defaultTableModel.setRowCount(0);
        int stt = 1;
        for (ThongKe x : _lstTK) {
            defaultTableModel.addRow(new Object[]{
                stt,
                x.getSp(),
                x.getSoSP()
            });
            stt++;
        }
    }

